def start_pressed():
    if input("CMD") == "s":
        return True
    return False

def pause_pressed():
    if input("CMD") == "p":
        return True
    return False

def quit_pressed():
    if input("CMD") == "q":
        return True
    return False

def end_pressed():
    if input("CMD") == "e":
        return True
    return False